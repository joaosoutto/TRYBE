// Adição
function sum(a, b) {
    return a + b;
}
console.log(sum(3, 7));

// Subtração
function sub(a, b) {
    return a - b;
}
console.log(sub(10, 5));

// Multiplicação
function sub(a, b) {
    return a * b;
}
console.log(sub(10, 5));

// Divisão
function sub(a, b) {
    return a / b;
}
console.log(sub(10, 5));

// Módulo
function sub(a, b) {
    return a % b;
}
console.log(sub(10, 5));